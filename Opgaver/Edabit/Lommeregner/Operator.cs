namespace Opgaver
{
    class Operator
    {
        //<>
        public double Plus(double result, double num2) => result + num2;
        public double Minus(double result, double num2) => result - num2;
        public double Gange(double result, double num2) => result * num2;
        public double Divider(double result, double num2) => result / num2;
    }
}
